#數字運算+-*/%
x=10/3
print(x)  #3.333

x=10//3
print(x)  #3

x=3//6
print(x)  #0

x=2**0.5 #次方 
print(x) #1.414  開根號

x+=1
print(x) # x+1 >> 2.414


